import java.util.*;
public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        
        System.out.println(n);
        // 시간복잡도 O(N) => 1
        // 1차원 배열은 항상 1이 나옴
        System.out.println(1);
    }
}